# (=, +=, -=, *=, /=, %=, **=)

num = 10
num += 10 # 10 + 10 => 20
print("num : ",num)

num -= 10 # 20 - 10 => 10
print("num : ",num)

num *= 10 # 10 * 10 => 100
print("num : ",num)

num /= 10 # 100 / 10 => 10.0
print("num : ",num)

num %= 10 # 10.0 % 10 => 0.0
print("num : ",num)

num **= 10 # 0.0 ** 0.0 => 0.0
print("num : ",num)



